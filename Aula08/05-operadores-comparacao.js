// Operadores comparação

var idadeDaPessoa = 13;
var idadeParaBeber = 18;

// Igualdade ==
console.log( idadeDaPessoa == idadeParaBeber );

// Diferença !=
console.log( idadeDaPessoa != idadeParaBeber );

// Maior que >
console.log( idadeDaPessoa > idadeParaBeber );

// Maior ou igual que >=
console.log( idadeDaPessoa >= idadeParaBeber );

// Menor que >
console.log( idadeDaPessoa < idadeParaBeber );

// Menor ou igual que >=
console.log( idadeDaPessoa <= idadeParaBeber );